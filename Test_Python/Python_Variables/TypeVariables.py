# ------------------------------
# Variables in Python
# ------------------------------

# A variable is like a box with a name where you store some data.
# Python does not require you to declare the type first (unlike Java or C++).
# Itâ€™s created automatically when you assign a value.

# ------------------------------
# Creating variables
# ------------------------------

x = 5  # integer value assigned to variable x
name = "Akash"  # string value assigned to variable name
price = 99.99  # float value assigned to variable price
is_valid = True  # boolean value assigned to variable is_valid

# ------------------------------
# Printing variable values
# ------------------------------

print(x)  # prints the value of x (5)
print(name)  # prints the value of name ("Akash")
print("Price:", price)  # prints "Price:" followed by the value of price (99.99)

# ------------------------------
# Checking variable types
# ------------------------------

# type() returns the data type of a variable
print(type(x))  # <class 'int'> because x is an integer
print(type(name))  # <class 'str'> because name is a string
print(type(price))  # <class 'float'> because price is a float
print(type(is_valid))  # <class 'bool'> because is_valid is a boolean
