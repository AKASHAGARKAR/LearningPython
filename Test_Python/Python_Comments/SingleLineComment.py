# ------------------------------
# Python Comments
# ------------------------------

# Comments can be used to explain Python code.
# Comments can be used to make the code more readable.
# Comments can be used to prevent execution when testing code.

# ------------------------------
# Single-line comment example
# ------------------------------

# This is a single line comment:
print("Hello, World!")  # This will print: Hello, World!

# You can also place a comment at the end of a line:
print("Hello, World!")  # This is a comment after code

# ------------------------------
# ðŸ”¹ Key Takeaways
# ------------------------------
# - Use '#' at the start of a line for a comment.
# - Anything after '#' on a line is ignored by Python.
# - Comments make code more understandable for humans.
# - You can temporarily "disable" code by turning it into a comment.
